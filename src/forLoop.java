public class forLoop {
    public static void main(String[] args) {
        //Declaring and integer,initializing and increment forLopp
        for(int i=0;i<10;i++) {
            //Using printf function because you need to print out the value of inetger
            System.out.printf("Java is becoming more interesting by %d\n", i);
            
            System.out.println();
            
            //Using the standard java function to print out with its increment value
            System.out.println("java is becoming more interesting by " + i); 
        }
    }

}
